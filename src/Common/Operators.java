package Common;

public class Operators {
    public static void main(String[]args){
        int l = 10;
        System.out.println(l++);
        System.out.println(l);
        System.out.println(l--);
        System.out.println(++l);
        System.out.println(--l);

        System.out.println(--l + ++l);
    }
}
